# Arithmetic Operator
print("5**3 =",5**3)  # Exponents
print("15//6 =",15//6) # output - 2; ignores decimal values
print("15/6 = ",15/6) # 2.5
print("15%/6 = ",15%6) # Remainder is 3
print("15%7 = ",15%7) # Remainder is 1
print("")

# Logical Operators
print(5 is 6)
print(5 is 5)
print(5 is not 6)
print("------------------------------------------------")
print( 5 in [1,2,3,4,5])
print( 5 not in [1,2,3,4,5])
